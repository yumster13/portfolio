package g58412.boulder.model;

/**
 * GameStates for the game
 *  -PLAY: the game is playing
 *  -PAUSED: the game is paused
 *  -STOPPED: the game is stopped, the player is dead
 *  -EXIT: The player has entered an exit and is going in a new Level
 */
public enum Gamestate {
    PLAY,PAUSED,STOPPED,EXIT

}
